// Deduplication helpers
export function findDuplicatePatterns(patterns: readonly AntiPatternV2[]): {
  duplicateIds: string[];
  similarPatterns: { idA: string; idB: string; reason: string }[];
} {
  const seenIds = new Set<string>();
  const duplicateIds: string[] = [];
  const similarPatterns: { idA: string; idB: string; reason: string }[] = [];

  for (const p of patterns) {
    if (seenIds.has(p.id)) {
      duplicateIds.push(p.id);
    }
    seenIds.add(p.id);
  }

  for (let i = 0; i < patterns.length; i++) {
    for (let j = i + 1; j < patterns.length; j++) {
      const a = patterns[i];
      const b = patterns[j];
      if (a.id === b.id) continue;
      if (a.category === b.category && a.symptom === b.symptom && a.rootCause === b.rootCause) {
        similarPatterns.push({ idA: a.id, idB: b.id, reason: 'Same category + symptom + rootCause' });
      }
    }
  }

  return { duplicateIds, similarPatterns };
}

export function validateUniquePatternIds(patterns: readonly AntiPatternV2[]): string[] {
  const seen = new Set<string>();
  const duplicates: string[] = [];
  for (const p of patterns) {
    if (seen.has(p.id)) {
      duplicates.push(p.id);
    }
    seen.add(p.id);
  }
  return duplicates;
}

export function validateRelatedPatternIds(patterns: readonly AntiPatternV2[]): string[] {
  const validIds = new Set(patterns.map((p) => p.id));
  const broken: string[] = [];
  for (const p of patterns) {
    for (const related of p.relatedPatterns) {
      if (!validIds.has(related)) {
        broken.push(`${p.id} -> ${related}`);
      }
    }
  }
  return broken;
}
